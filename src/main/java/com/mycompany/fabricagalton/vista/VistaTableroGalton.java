    private void construccionTablero(){
        int filas = tableroGalton.getNFilas();
        panelTablero.setLayout(new GridLayout(filas,1));
        paneles = new JPanel[filas];
        celdas = new JLabel[filas][];
        for (int i = 0; i < filas; i++) {
            paneles[i] = new JPanel();
            celdas[i] = new JLabel[i+1];
            for (int j = 0; j < i+1; j++) {
                celdas [i][j] = this.celdaVacia();
                FlowLayout flowLayout = new FlowLayout();
                flowLayout.setHgap(50);
                paneles[i].setLayout(flowLayout);
                paneles[i].setBackground(Color.lightGray);
                paneles[i].add(celdas[i][j]);
            }
            this.panelTablero.add(paneles[i]);
        }
        depositos = new JLabel[filas+1];
        ((FlowLayout)panelDepositos.getLayout()).setHgap(50);
        for (int i = 0; i < filas+1; i++) {
            depositos[i]= new JLabel("0");
            depositos[i].setHorizontalAlignment(JLabel.CENTER);
            depositos[i].setVerticalAlignment(JLabel.CENTER);
            depositos[i].setPreferredSize(new Dimension(40,40));
            panelDepositos.add(depositos[i]);
        }
        this.setSize(1200,800);
    }
    
    private JLabel celdaVacia (){
        JLabel celda = new JLabel();
        celda.setLayout(new BorderLayout());
        celda.setPreferredSize(new Dimension (40,40));
        celda.setHorizontalAlignment(JLabel.CENTER);
        celda.setVerticalAlignment(JLabel.CENTER);
        celda.setOpaque(true);
        celda.setBackground(Color.white);
        return celda;
    }
    private void dibujarCeldaBola(JLabel celda,Bola bola){
        vaciarCelda(celda);
        if(bola != null){
            int indice = bola.getNumero()%5;
            celda.setIcon(new ImageIcon("imagenes\\bola"+indice+".jpg"));
            JLabel etiquetaTexto = new JLabel (bola.getNumero()+"");
            etiquetaTexto.setHorizontalAlignment(JLabel.CENTER);
            etiquetaTexto.setVerticalAlignment(JLabel.CENTER);
            celda.add(etiquetaTexto);
            
        }
    }
    
    public void actualizarDibujoTablero(){
        int filas = tableroGalton.getNFilas();
        for (int i = 0; i < filas; i++) {
            for (int j = 0; j < i+1; j++) {
                
                dibujarCeldaBola(celdas[i][j],tableroGalton.getContenidoCelda(i, j));
                this.repaint();
            }
        }
        for (int i = 0; i < depositos.length; i++) {
            depositos[i].setText(tableroGalton.getDeposito(i).size()+"");
        }
    }
    
    private void vaciarCelda(JLabel celda){
        celda.setIcon(null);
        celda.removeAll();
    }
    private void vaciarDepositos() {
        tableroGalton.iniciarDepositos(depositos.length);
        for(int i=0;i<depositos.length;i++) {
            depositos[i].setText("0");
        }
    }

